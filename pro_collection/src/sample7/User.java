package sample7;

public class User {
	

	private String name;
	private String types;
	
	public User(String name, String types) {
		super();
		this.name = name;
		this.types = types;
	}
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getTypes() {
		return types;
	}

	public void setTypes(String types) {
		this.types = types;
	}

	
	
}
